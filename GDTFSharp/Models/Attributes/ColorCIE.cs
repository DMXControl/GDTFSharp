namespace GDTFSharp
{
    public class ColorCIE
    {
        public float floatx { get; }
        public float floaty { get; }
        public float floatY { get; }

        public ColorCIE(float floatx, float floaty, float floatY)
        {
            this.floatx = floatx;
            this.floaty = floaty;
            this.floatY = floatY;
        }
    }
}